package a.basic.pratice;

public class Operator {
    public static void main(String[] args) {
        int intA = 14;
        int intB = 6;
        System.out.println(intA & intB);

        int inA =14;
        int inB = 6;
        System.out.println(inA & intB);

        int intC = 123456;
        System.out.println(intC << 1);
        System.out.println(intC >> 1);

        System.out.println(intC >> 1);
        System.out.println(intC >>> 1);



    }
}
