package a.basic.pratice;

public class Variable {
    public static void main(String[] args) {

        // 선언
        // 타입 변수명 ;
        int age;

        // 할당
        //변수명 = 값;
        age = 20;

        System.out.printf("나이 %d", age);

        //선언 + 할당 같이
        String name = "KIM";
        System.out.printf("이름 %s", name);

    }
}
